# --- Kilometers to Miles Conversion ---
km = float(input("Enter distance in kilometers: "))  # user se km input
miles = km * 0.621371  # conversion formula
print(f"{km} km = {miles} miles")

# --- Celsius to Fahrenheit Conversion ---
celsius = float(input("Enter temperature in Celsius: "))  # user se temp input
fahrenheit = (celsius * 9/5) + 32  # conversion formula
print(f"{celsius} °C = {fahrenheit} °F")
